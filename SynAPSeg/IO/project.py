    def filter_example_contents(self, required_filenames, return_matches=False):
        """
        helper function to filter examples by whether they have required filenames

        Args:
            required_filenames: list(str | list) list of filenames to get
                strings should include filename and suffix (not full path)
                list elements represent analogous files that have different names e.g. ROI_0.tiff and ROI_0_ch0.tiff
                example:
                    required_filenames = ['mip_raw.tiff', ['ROI_0.tiff', 'ROI_0_edited.tiff'], 'ROI_1.tiff']
                    would get mip_raw, ROI_1, and either ROI_0 or, if not present, would look for ROI_0_edited
                
        """
        
        self.filter_examples(
            lambda ex: ex.check_example_filenames(required_filenames)[0]
        )

        if return_matches:
            ex_dict_matches = {}
            for ex in self.examples:
                ex_dir_contents = ex.get_filenames()
                matches = []
                for req_fn in required_filenames:
                    if isinstance(req_fn, str) and (req_fn in ex_dir_contents):
                        matches.append(req_fn)
                    elif isinstance(req_fn, list):
                        for _fn in req_fn:
                            if _fn in ex_dir_contents:
                                matches.append(_fn)
                                break
                ex_dict_matches[ex.name] = matches
            return ex_dict_matches

