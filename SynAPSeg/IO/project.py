    def batch_attach_data(self, new_data_mds:list[dict], example_names: Optional[str | list[str]] = None) -> None:
        """ 
        Update metadata for several examples in a project, meant for attaching array format info to new data
            add datakey (filename without suffix), format, and shape (optional) to metadata.
                # TODO shape peek and ensure format matches existing array
            by default (example_names=None) applies to all examples
            only adds if the entry's key points to a file that exists in the example
        
        # TODO - protect adding formating - should do all the standard checks whenever format/shape is user-provided
        
        args:
            new_data_mds: - providing shape is optional if the filepath to added array data is found and parsed by uip.get_tiff_shape
                e.g. [{'key':'ROI_0', 'data_formats':'YX', 'data_shapes':[1908, 1908]}]
            example_names: 
                if not provided, applies to all examples in the project
                e.g. '0000' or ['0000', '0001', ...]
        """
        from SynAPSeg.utils.utils_image_processing import get_tiff_shape
        upd_keys = ['data_formats', 'data_shapes']

        example_names = None if not example_names else [example_names] if not isinstance(example_names, list) else example_names
        
        exmpls: list[Example]
        exmpls = self.examples if example_names is None else [self.get_example(name) for name in example_names]

        counter_update_sucesses = 0
        for ex in exmpls:
            _entry_success = 0
            for entry in new_data_mds:
                data_name = entry['key']

                for k in upd_keys:
                    if not ex.has_data(data_name): 
                        continue

                    # help get array shape if not provided
                    try:
                        shape = get_tiff_shape(ex.get_path(f"{data_name}.tiff"))
                    except:
                        shape = None
                    ex.exmd['data_metadata']['data_shapes'][data_name] = shape

                    if k in entry.keys():
                        ex.exmd['data_metadata'][k][data_name] = entry[k]
                        _entry_success += 1

            if _entry_success > 0:
                counter_update_sucesses+=1
                ex.write_metadata()
        
        if counter_update_sucesses < 1:
            raise KeyError(f"attempted metadata entry did not apply to any examples")
        
        print(f"succesfully updated metadata for {counter_update_sucesses}/{len(exmpls)} examples")


    def validate_all_examples_data_formats(
            self, 
            raw_img_format=None,
            required_files: Optional[List[str]] = None,
            excluded_files: Optional[List[str]] = None) -> Dict:
        """
        Validate data formats for all examples in the project
        
        Args:
            required_files: List of files that must be present
            excluded_files: List of files to exclude from validation
            
        Returns:
            Dictionary with validation results for all examples
        """
                  
        validation_results = {
            'passed_examples': [],
            'failed_examples': [],
            'validation_details': {},
            'overall_success': True
        }
        
        for example in self.examples:
            try:
                result = example.validate_and_update_data_formats(
                    raw_img_format=raw_img_format,
                    required_files=required_files,
                    excluded_files=excluded_files
                )
                
                validation_results['validation_details'][example.name] = result
                
                if result['validation_passed']:
                    validation_results['passed_examples'].append(example.name)
                else:
                    validation_results['failed_examples'].append(example.name)
                    validation_results['overall_success'] = False
                    
            except Exception as e:
                validation_results['failed_examples'].append(example.name)
                validation_results['validation_details'][example.name] = {
                    'error': str(e),
                    'validation_passed': False
                }
                validation_results['overall_success'] = False
        
        return validation_results
    
