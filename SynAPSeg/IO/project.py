    def batch_update_metadata(
            self, 
            raw_img_format=None,
            required_files=None, 
            excluded_files=None,
            validate_formats: bool = True, 
            update_scaling: bool = False, 
            write_changes: bool = False
        ) -> Dict:
        """
        Batch update metadata for all examples
        
        Args:
            validate_formats: Whether to validate and update data formats
            update_scaling: Whether to update image scaling information
            write_changes: Whether to write changes to disk
            
        Returns:
            Dictionary with comprehensive results
        """
        results = {
            'format_validation': None,
            'scaling_updates': None,
            'write_results': None,
            'overall_success': True
        }
        
        if validate_formats:
            print("Validating data formats...")
            results['format_validation'] = self.validate_all_examples_data_formats(
                raw_img_format=raw_img_format,
                required_files=required_files, 
                excluded_files=excluded_files
            )
            if not results['format_validation']['overall_success']:
                results['overall_success'] = False
                print(f"Format validation failed for: {results['format_validation']['failed_examples']}")
        
        if update_scaling:
            print("Updating scaling metadata...")
            results['scaling_updates'] = self.update_all_examples_scaling()
            if results['scaling_updates']['failed_examples']:
                print(f"Scaling update failed for: {results['scaling_updates']['failed_examples']}")
        
        if write_changes and results['overall_success']:
            print("Writing metadata changes...")
            write_results = {'successful_writes': [], 'failed_writes': []}
            
            for example in self.examples:
                try:
                    self.get_metadata_parser().write_metadata(example.path_to_example, example.exmd)
                    write_results['successful_writes'].append(example.name)
                except Exception as e:
                    write_results['failed_writes'].append((example.name, str(e)))
            
            results['write_results'] = write_results
        elif write_changes and not results['overall_success']:
            print("Skipping write due to validation failures")
        
        return results

