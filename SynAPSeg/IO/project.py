class DataFormatValidator:
    """Utility class for validating and inferring data formats"""
    
    @staticmethod
    def infer_format_from_shape(shape: Tuple[int], base_shape_mapping: Dict[str, int]) -> str:
        """
        Infer axis format by matching dimensions to known base dimensions
        
        Args:
            shape: Tuple of dimensions from actual data
            base_shape_mapping: Dictionary mapping axis labels to their expected sizes
            
        Returns:
            String representing the inferred format (e.g., 'YXC')
        """
        available_axes = list(base_shape_mapping.items())
        inferred_format = ''
        
        for dimension in shape:
            found_match = False
            for axis, size in available_axes:
                if dimension == size:
                    inferred_format += axis
                    available_axes.remove((axis, size))
                    found_match = True
                    break
            
            if not found_match:
                raise ValueError(f"Could not match dimension {dimension} to any available axis")
        
        return inferred_format
    
    @staticmethod
    def validate_shape_format_consistency(
        shape: Tuple[int], 
        format_str: str, 
        base_shape_mapping: Dict[str, int]
    ) -> dict:
        """
        Validate that a shape and format string are consistent
        
        Returns:
            Dictionary with validation results
        """
        # Build axis-to-shape mapping from format string
        axis_shape_mapping = {}
        for i, axis in enumerate(format_str):
            if i >= len(shape):
                return {'length_match': False, 'shape_consistency': False}
            axis_shape_mapping[axis] = shape[i]
        
        # Rebuild shape from format
        rebuilt_shape = [axis_shape_mapping[axis] for axis in format_str]
        
        return {
            'length_match': len(format_str) == len(shape),
            'shape_consistency': list(shape) == rebuilt_shape,
            'axis_mapping': axis_shape_mapping,
        }





class Example:
    """ directory containing image data """

    def __init__(self, path_to_example:str, make:bool=False):
        self.path_to_example = path_to_example
        self.name: str = Path(self.path_to_example).name
        self.exmd = {}
        self.project_name = Path(self.path_to_example).parts[-3]

        self.initialize_example(make=make)
    
    def initialize_example(self, make=False):
        if not make: assert os.path.exists(self.path_to_example)
        ug.verify_outputdir(self.path_to_example, makedirs=make)
        self.exmd = self.get_metadata_parser().try_get_metadata(self.path_to_example, FILE_MAP=None, silent=True)
        self.is_complete = self.check_marked_complete(self.path_to_example)
        self.is_skipped = self.check_marked_skip(self.path_to_example)
    
    @staticmethod
    def is_ex_dir(path_to_example):
        """check if path is an example directory by looking if it contains metadata.yml file"""
        if not os.path.isdir(path_to_example):
            return False    
        if not "metadata.yml" in os.listdir(path_to_example):
            return False
        return True
    
    @staticmethod
    def check_marked_complete(path_to_example):
        """check if an example directory is complete by looking if it contains a 'complete.txt' file"""
        IS_COMPLETE = True if 'complete.txt' in os.listdir(path_to_example) else False
        return IS_COMPLETE
    
    @staticmethod
    def check_marked_skip(path_to_example):
        """check if an example directory is complete by looking if it contains a 'complete.txt' file"""
        IS_SKIP = True if "__skip_this_example.txt" in os.listdir(path_to_example) else False
        return IS_SKIP
    
    def set_logger(self, logger):
        self.logger = logger
    
    def get_metadata_parser(self) -> type['MetadataParser']:
        """ returns metadata parser class - exists to centralize and avoid import conflicts """
        return get_MetadataParser()
    
    def log(self, msg, lvl='info'):
        if hasattr(self, "logger"):
            getattr(self.logger, lvl)(msg)

    def get_image_path(self):
        """ get the path of the examples source image """
        img_path = self.exmd.get('image_path', self.exmd.get('czi_path'))
        return img_path
    
    def get_path(self, fn:str) -> str:
        """ return the full path to a filename, if present else Raise error """
        p = os.path.join(self.path_to_example, fn)
        if not os.path.exists(p):
            raise FileNotFoundError(f"{fn} not found in example: {self.name}")
        return p
        

    def unmark_complete(self) -> None:
        """ deletes complete.txt from the example dir """
        cpath = os.path.join(self.path_to_example, 'complete.txt')
        if os.path.exists(cpath):
            os.remove(cpath)
        
    def mark_complete(self) -> None:
        """
        Marks the example as complete, for instance by writing a 'complete.txt' file in the example directory.
        """
        try:
            complete_filepath = os.path.join(self.path_to_example, "complete.txt")
            with open(complete_filepath, "w") as f:
                f.write("Processing complete.")
            self.log(f"Marked complete at {complete_filepath}.")
            
        except Exception as e:
            self.log(f"Error marking complete: {e}", lvl='error')
            raise
    
    def write_metadata(self) -> None:
        """ write the current example.exmd (metadata) to disk, overwrites existing version """
        self.get_metadata_parser().write_metadata(self.path_to_example, self.exmd)
    
