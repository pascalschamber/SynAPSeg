    @staticmethod
    def get_dir_progress(
        proj=None,
        FILE_MAP=None, 
        dir_examples=None, 
        ) -> tuple[pd.DataFrame, dict[str, list[Any]], int, int, int, dict[str, list[str]]]:
        """ 
        inspect and display overview of each example's status, src filename, and annotated files in a project 
        
        Args:
            - proj (optionally provide a project object) else must be None if passing path to dir to check through dir_examples
            - FILE_MAP (dict(str:list|str)) - only mark complete each value (list of possible filenames to look for, or str)
                note: if dict(str, list) is passed, if any fn in each list is found will return true to
                 allow for checking if one of several options is found.
                note: keys in filemap are ignored and only values are used for checking.
            - dir_examples (optional if project is provided) else must be path to examples directory
        returns:
            info_df, where_map, complete_count, has_annots, not_started_count, annotated_file_names
        """
        
        if not proj:
            assert isinstance(dir_examples, str) and os.path.exists(dir_examples)
            pth = Path(dir_examples)
            proj = Project(pth.parent, examples_dir_name=pth.stem)
        else:
            assert dir_examples is None
            dir_examples = proj.path_to_examples
        FILE_MAP = FILE_MAP or {}
        run_filemap_check = len(FILE_MAP)>0 # run check if not empty
        
        # collect example statuses
        where_map={'complete':[], 'skipped':[], 'has_annots':[], 'started':[], 'not_started':[]}
        complete_count=0
        skipped_count=0
        has_annots=0
        not_started_count=0

        # iter examples
        annotated_filenames_map = {}
        tab_table = []
        for example in proj.examples:
            assert isinstance(example, Example)
            
            amd = example.exmd.get('annotation_metadata', {})            
            if not isinstance(amd, dict): # e.g. 'notStarted'
                amd = {}
            ex_status = amd.get('status','')
            ex_notes = amd.get('notes','')
            ex_scenename = example.exmd.get('scene_name', '')
            
            # get anotated files in an examples dir
            contents = example.get_filenames(pattern='.*')
            ann_fns = example.get_annotations(ex_contents=contents) or []
            annotated_filenames_map[example.name] = ann_fns
            if len(ann_fns) > 0:
                where_map['has_annots'].append(example.path_to_example)
                has_annots+=1
            
            # check all files provided in the filemap exist in this examples directory
            if run_filemap_check:
                filemap_check_passed, filemap_check_errors = example.check_example_filenames(list(FILE_MAP.values()))
            else:
                filemap_check_passed, filemap_check_errors = None, []
            
            # track overall progress for this project
            if example.is_skipped:
                where_map['skipped'].append(example.path_to_example)
                skipped_count += 1
                ex_status += 'SKIP'
            elif example.is_complete and (filemap_check_passed is not False):
                where_map['complete'].append(example.path_to_example)
                complete_count += 1
                ex_status += 'COMPLETE'
            else:
                where_map['not_started'].append(example.path_to_example)
                not_started_count+=1

            # build display table 
            tab_vars = [example.name, ex_status, Path(example.get_image_path()).stem, ex_scenename, filemap_check_passed, filemap_check_errors, len(ann_fns), ann_fns, ex_notes]
            tab_table.append(tab_vars)

        print(proj.name)
        table_headers = ['ex_i', 'status', 'image_filename', 'scene_name', 'filemap_check', 'filemap_check_failures', 'annotated_count', 'annotated_filenames', 'notes']
        print(tabulate(tab_table, headers=table_headers, tablefmt="heavy_outline"))
        info_df = pd.DataFrame(tab_table, columns=table_headers)
        return  info_df, where_map, complete_count, has_annots, not_started_count, annotated_filenames_map
    
