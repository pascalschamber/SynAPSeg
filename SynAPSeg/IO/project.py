    def filter_examples(self, condition, **kwargs) -> None:
        """
        Updates self.examples with those that meet the given condition. 
            Those that do not meet condition will be added to self.excluded_examples
        
        Args:
            condition: A callable that takes an Example and returns True/False.
            kwargs: are passed to condition function
        
        Returns:
            None
        """
        exs = []
        exlc_exs = []
        for ex in self.examples:
            res = condition(ex, **kwargs)
            if res:
                exs.append(ex)
            else:
                exlc_exs.append(ex)
        self.set_examples(exs)
        self.excluded_examples.extend(exlc_exs)
    
