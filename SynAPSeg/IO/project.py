    def get_filenames(
            self, 
            pattern: str = r'.*\..*',
            excluded_files: Optional[List[str]] = None,
            fail_on_empty=True,
            warn=True,
        ) -> list[str]:
        """
        Get list of files present in the example directory that match the pattern provided
        
        Args:
            pattern: used for re.match, defaults to grabbing all files
                e.g. '(?i)\\.tiff?\\Z' <-- example to match str ending with .tif or .tiff & is case-insensitive
            excluded_files: List of files to exclude, should be formated as filename with suffix
                e.g. ['ROI_0.tiff']
            fail_on_empty: raises error if not found
            warn: print warning if not found and not fail_on_empty
            
        Returns:
            List of filenames found in the example directory
        """
        if excluded_files is None:
            excluded_files = []

        files = ug.get_contents(self.path_to_example, filter_str=pattern, pattern=True, fail_on_empty=fail_on_empty, warn=warn)
        files = [Path(fp).name for fp in files if Path(fp).name not in excluded_files]
        return files
    
    def check_example_filenames(self, required_filenames:list) -> tuple[bool, list]:
        """ 
        Check if all required filenames are in an examples contents

        Args:
            required_filenames: list of filenames or list[list[str]] to check if any of inner list is present
        Returns:
            whether check passed
            list of filenames that failed check    
        """
        ex_dir_contents = self.get_filenames(pattern='.*')
        matches, errors = [], []
        for req_fn in required_filenames:
            if len(req_fn) == 0: # skip empty lists/strings
                continue 
            elif isinstance(req_fn, str):
                passed = req_fn in ex_dir_contents
            elif isinstance(req_fn, list):
                passed = ug.any_isin(ex_dir_contents, req_fn)
            else:
                raise ValueError(f"required_filenames elements must be either string or list but got: {req_fn}")
            
            matches.append(passed)
            if not passed:
                errors.append(req_fn)

        return all(matches), errors
    
    def get_annotations(self, ex_contents=None, ann_fn_prefixes=['annotated_', 'ROI_']):
        """ 
            get list of annotated filenames in an examples directory 

            Args:
                ann_fn_prefixes (List[str]): filenames that start with any of these strings are considered annotations
        """
        ex_contents = ex_contents or self.get_filenames()
        ann_fns = [c for c in ex_contents if any([c.startswith(pre) for pre in ann_fn_prefixes])]
        return ann_fns
        
    def has_data(self, data_key:str):
        """ 
        returns true if the key points to a filename that exists in the example 
            TODO only supports keys with filenames sans suffix, future should support if suffix is included
        """
        return self._prefix_in_ex_files(data_key)
    
